package com.deviceinsight.bumbershoot.exception;


@SuppressWarnings("serial")
public class ChartUploadException extends Exception {

	public ChartUploadException() {
		super();
	}

	public ChartUploadException(String message, Throwable cause) {
		super(message, cause);
	}

	public ChartUploadException(String message) {
		super(message);
	}

	public ChartUploadException(Throwable cause) {
		super(cause);
	}


}
