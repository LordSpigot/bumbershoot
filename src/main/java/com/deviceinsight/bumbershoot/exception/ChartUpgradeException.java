package com.deviceinsight.bumbershoot.exception;

@SuppressWarnings("serial")
public class ChartUpgradeException extends Exception {

	public ChartUpgradeException() {
	}

	public ChartUpgradeException(String message, Throwable cause) {
		super(message, cause);
	}

	public ChartUpgradeException(String message) {
		super(message);
	}

	public ChartUpgradeException(Throwable cause) {
		super(cause);
	}

	
}
